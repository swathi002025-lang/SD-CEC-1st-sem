#include<stdio.h>
int main(){
    int number = 15;
    printf("number contains: %d\n", number);   
    printf("number contains(pre increment): %d\n", ++number);
    printf("number contain:%d\n",number);
    printf("number contains(post increment): %d\n", number++);
    printf("number contain:%d\n",number);
    printf("number contains(pre decrement): %d\n", --number);
    printf("number contain:%d\n",number);
    printf("number contains(post decrement): %d\n", number--);
    printf("number contain:%d\n",number);
    
}